package com.fundamental.model;

/**
 * @author Henry Barrientos
 */
public class ArqueocajaBomba {

    private Integer arqueocajaId;
    private Integer bombaId;
    private Integer turnoId;
    private String descError;

    public ArqueocajaBomba(Integer arqueocajaId, Integer bombaId, Integer turnoId) {
        this.arqueocajaId = arqueocajaId;
        this.bombaId = bombaId;
        this.turnoId = turnoId;
    }

    public ArqueocajaBomba() {
    }

    public Integer getArqueocajaId() {
        return arqueocajaId;
    }

    public void setArqueocajaId(Integer arqueocajaId) {
        this.arqueocajaId = arqueocajaId;
    }

    public Integer getBombaId() {
        return bombaId;
    }

    public void setBombaId(Integer bombaId) {
        this.bombaId = bombaId;
    }

    public String getDescError() {
        return descError;
    }

    public void setDescError(String descError) {
        this.descError = descError;
    }

    public Integer getTurnoId() {
        return turnoId;
    }

    public void setTurnoId(Integer turnoId) {
        this.turnoId = turnoId;
    }
    
}
