package com.fundamental.model;

/**
 * @author Henry Barrientos
 */
public class TurnoEmpleadoBomba {

    //required
    private Integer fakeId, turnoId, empleadoId, bombaId;
    private String creadoPor;
    //optional
    private String creadoEl, modificadoPor, modificadoEl;
    //
    private Empleado employee;
    private Bomba pump;

    public TurnoEmpleadoBomba(int fakeId, int turnoId, int empleadoId, int bombaId, String creadoPor) {
        this.fakeId = fakeId;
        this.turnoId = turnoId;
        this.empleadoId = empleadoId;
        this.bombaId = bombaId;
        this.creadoPor = creadoPor;
    }

    public Integer getFakeId() {
        return fakeId;
    }

    public void setFakeId(Integer fakeId) {
        this.fakeId = fakeId;
    }

    public Integer getTurnoId() {
        return turnoId;
    }

    public void setTurnoId(Integer turnoId) {
        this.turnoId = turnoId;
    }

    public Integer getEmpleadoId() {
        return empleadoId;
    }

    public void setEmpleadoId(Integer empleadoId) {
        this.empleadoId = empleadoId;
    }

    public Integer getBombaId() {
        return bombaId;
    }

    public void setBombaId(Integer bombaId) {
        this.bombaId = bombaId;
    }

    public String getCreadoPor() {
        return creadoPor;
    }

    public void setCreadoPor(String creadoPor) {
        this.creadoPor = creadoPor;
    }

    public String getCreadoEl() {
        return creadoEl;
    }

    public void setCreadoEl(String creadoEl) {
        this.creadoEl = creadoEl;
    }

    public String getModificadoPor() {
        return modificadoPor;
    }

    public void setModificadoPor(String modificadoPor) {
        this.modificadoPor = modificadoPor;
    }

    public String getModificadoEl() {
        return modificadoEl;
    }

    public void setModificadoEl(String modificadoEl) {
        this.modificadoEl = modificadoEl;
    }

    public Empleado getEmployee() {
        return employee;
    }

    public void setEmployee(Empleado employee) {
        this.employee = employee;
    }

    public Bomba getPump() {
        return pump;
    }

    public void setPump(Bomba pump) {
        this.pump = pump;
    }

}
