package com.fundamental.model;

import java.util.List;

/**
 * @author Henry Barrientos
 */
public class Empleado {

    private int empleadoId;
    private String nombre, estado, creadoPor, creadoEl;
    private String modificadoPor, modificadoEl;
    //
    private List<Bomba> bombas;
    private Arqueocaja arqueo;

    public Empleado(int empleadoId, String nombre) {
        this.empleadoId = empleadoId;
        this.nombre = nombre;
    }
    
    public Empleado() {
    }
    
    public Empleado(int empleadoId, String nombre, String estado) {
        this.empleadoId = empleadoId;
        this.nombre = nombre;
        this.estado = estado;
    }

    public int getEmpleadoId() {
        return empleadoId;
    }

    public void setEmpleadoId(int empleadoId) {
        this.empleadoId = empleadoId;
    }

    public String getNombre() {
        return nombre.toUpperCase();
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getCreadoPor() {
        return creadoPor;
    }

    public void setCreadoPor(String creadoPor) {
        this.creadoPor = creadoPor;
    }

    public String getCreadoEl() {
        return creadoEl;
    }

    public void setCreadoEl(String creadoEl) {
        this.creadoEl = creadoEl;
    }

    public List<Bomba> getBombas() {
        return bombas;
    }

    public void setBombas(List<Bomba> bombas) {
        this.bombas = bombas;
    }

    public String getModificadoPor() {
        return modificadoPor;
    }

    public void setModificadoPor(String modificadoPor) {
        this.modificadoPor = modificadoPor;
    }

    public String getModificadoEl() {
        return modificadoEl;
    }

    public void setModificadoEl(String modificadoEl) {
        this.modificadoEl = modificadoEl;
    }

    public Arqueocaja getArqueo() {
        return arqueo;
    }

    public void setArqueo(Arqueocaja arqueo) {
        this.arqueo = arqueo;
    }

}
